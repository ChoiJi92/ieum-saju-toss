export type GunghapResult = {
  totalScore: number;
  tagline: string;
  axes: Array<{ lbl: string; ic: string; score: number; color: string; body: string }>;
  comment: string;
  hap: boolean;
  myIlgan: Stem;
  otherIlgan: Stem;
  sipsung: Sipsung;
  strengths: string[];
  cautions: string[];
  dates: string[];
  longTerm: string;
};

function variance(seed: string, key: string, range = 3): number {
  let h = 0;
  const s = seed + key;
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) | 0;
  return (Math.abs(h) % (range * 2 + 1)) - range;
}

/** 두 사람 일간 + 시드 → 궁합 (같은 일간끼리도 다른 사주끼리 다른 점수) */
export function calcGunghap(myIlgan: Stem, otherIlgan: Stem, seed = ''): GunghapResult {
  const sipsung = getSipsung(myIlgan, otherIlgan);
  const baseAxes = AXIS_BY_SIPSUNG[sipsung];
  const adj = (k: string, v: number) =>
    Math.max(50, Math.min(98, v + variance(seed, k, 3)));
  const axes = {
    성격: adj('성격', baseAxes.성격),
    대화: adj('대화', baseAxes.대화),
    연애: adj('연애', baseAxes.연애),
    돈:   adj('돈',   baseAxes.돈),
  };
  const isHap = HAP[myIlgan] === otherIlgan;

  // 5합이면 +5 보너스
  const bonus = isHap ? 5 : 0;
  const total = Math.min(
    98,
    Math.round((axes.성격 + axes.대화 + axes.연애 + axes.돈) / 4) + bonus
  );

  const taglineMap: Record<Sipsung, string> = {
    비견: '편안한 친구 같은 사이',
    겁재: '서로 밀고 당기는 사이',
    식신: '서로 결을 잘 알아주는 사이',
    상관: '재치·자극이 빛나는 사이',
    정재: '꾸준히 함께 갈 사이',
    편재: '활기·재미가 넘치는 사이',
    정관: '진중하게 챙겨주는 사이',
    편관: '강한 끌림과 도전의 사이',
    정인: '깊은 정신적 교감의 사이',
    편인: '미스터리한 끌림의 사이',
  };

  const axisBodies = AXIS_BODY_BY_SIPSUNG[sipsung];
  const detail = DETAIL_BY_SIPSUNG[sipsung];

  return {
    totalScore: total,
    tagline: isHap ? `${taglineMap[sipsung]} (5합 ✨)` : taglineMap[sipsung],
    axes: [
      { lbl: '성격 합',   ic: '☁️', score: axes.성격, color: '#9D7BFF', body: axisBodies.성격 },
      { lbl: '대화 합',   ic: '💬', score: axes.대화, color: '#3DC795', body: axisBodies.대화 },
      { lbl: '연애 합',   ic: '💞', score: axes.연애, color: '#F495C9', body: axisBodies.연애 },
      { lbl: '돈 가치관', ic: '💰', score: axes.돈,   color: '#FFC857', body: axisBodies.돈 },
    ],
    comment: COMMENT_BY_SIPSUNG[sipsung],
    hap: isHap,
    myIlgan,
    otherIlgan,
    sipsung,
    strengths: detail.strengths,
    cautions: detail.cautions,
    dates: detail.dates,
    longTerm: detail.longTerm,
  };
}
