/**
 * 날짜 기반 시드 문자열 — YYYYMMDD 형식.
 * 이 시드를 rotateBySeed의 seed에 섞으면 매일 다른 문구가 선택돼요.
 */
export function todayDateSeed(): string {
  const d = new Date();
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, '0');
  const day = String(d.getDate()).padStart(2, '0');
  return `${y}${m}${day}`;
}

/**
 * 신살별 오늘의 활용·주의 문구 한 쌍을 반환해요.
 * pillarSeed(명식 고유값) + 오늘 날짜를 합쳐 매일 다른 문구가 나와요.
 */
export function getSinsalDailyTips(
  name: string,
  pillarSeed: string,
): { lean: string; watch: string } {
  const pool = SINSAL_TIP_POOLS[name];
  if (!pool) {
    return {
      lean: '이 기운이 긍정적으로 발현될 때 적극적으로 활용해요.',
      watch: '이 기운의 부정적인 면을 인식하고 균형을 유지해요.',
    };
  }
  const dateSeed = todayDateSeed();
  const seed = `${pillarSeed}:${dateSeed}`;
  const [lean] = rotateBySeed(seed, `${name}:lean`, pool.lean, 1);
  const [watch] = rotateBySeed(seed, `${name}:watch`, pool.watch, 1);
  return { lean, watch };
}

