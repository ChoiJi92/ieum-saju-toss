/** 일간 + 비교 일간(예: 오늘 일진 천간) → DailyFortune */
export function fortuneBySipsung(ilgan: Stem, dayStem: Stem): DailyFortune {
  const sipsung = getSipsung(ilgan, dayStem);
  return FORTUNE_BY_SIPSUNG[sipsung];
}

/**
 * 명식 8자 시드 + 일진 → 점수에 작은 변동 (-3 ~ +3) 적용.
 * 같은 일간이라도 다른 사주(년·월·시 다르면)에 다른 점수.
 */
function seededVariance(seed: string, key: string, range = 3): number {
  let h = 0;
  const s = seed + key;
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) | 0;
  // -range ~ +range 범위
  return (Math.abs(h) % (range * 2 + 1)) - range;
}

/**
 * 본인 명식(8자) + 오늘 일진 → DailyFortune.
 * 십성으로 base 결과 결정 + 명식 시드로 점수 ±3 변동.
 * 카피 oneLine에 일간 한자 자연 삽입.
 */
export function personalizedFortune(
  ilgan: Stem,
  dayStem: Stem,
  myeongsikSeed: string
): DailyFortune {
  const base = fortuneBySipsung(ilgan, dayStem);
  // 시드 = 본인 명식 8자 + 오늘 일진 (날짜별로도 변동)
  const seed = myeongsikSeed + dayStem;
  const adjust = (key: string, score: number) =>
    Math.max(50, Math.min(98, score + seededVariance(seed, key, 3)));

  return {
    ...base,
    sections: {
      overall: { score: adjust('overall', base.sections.overall.score), body: base.sections.overall.body },
      love:    { score: adjust('love',    base.sections.love.score),    body: base.sections.love.body },
      money:   { score: adjust('money',   base.sections.money.score),   body: base.sections.money.body },
      work:    { score: adjust('work',    base.sections.work.score),    body: base.sections.work.body },
      health:  { score: adjust('health',  base.sections.health.score),  body: base.sections.health.body },
    },
    // 일간 한글 + 오행 한글 (예: "기토 일간 ...") — 본문 한자 단독 X 가이드
    oneLine: `${stemKr(ilgan)}${ohaengKr(ilgan)} 일간 ${base.oneLine}`,
  };
}

function stemKr(s: Stem): string {
  const m: Record<Stem, string> = {
    甲: '갑', 乙: '을', 丙: '병', 丁: '정', 戊: '무',
    己: '기', 庚: '경', 辛: '신', 壬: '임', 癸: '계',
  };
  return m[s];
}

function ohaengKr(s: Stem): string {
  const m: Record<OhaengKey, string> = {
    wood: '목', fire: '화', earth: '토', metal: '금', water: '수',
  };
  return m[STEM_INFO[s].ohaeng];
}
